/*
数据：对客观事物的符号表示，在计算机科学中是指所有能输入到计算机中并被计算机程序处理的符号的总称

数据元素：是数据的基本单位，在计算机程序中通常作为一个整体进行考虑和处理。有时，一个数据元素可由若干个数据项组成，数据项是数据的不可分割的最小单位

数据对象：性质相同的数据元素的集合，是数据的一个子集

数据结构：相互之间存在一种或多种特定关系的数据元素的集合。通常有4类基本结构：集合、线性结构、树形结构、图状结构或网状结构

存储结构：数据结构在计算机中的表示。

数据类型：是一个值的集合和定义在这个值集上的一组操作的总称，例如c++中整型

抽象数据：指一个数据模型以及定义在该模型上的一组操作
*/